binary = 0b10100  # 二进制表示，前缀0b
octal = 0o24  # 八进制表示，前缀0o
decimal = 20  # 十进制表示
hexadecimal = 0x14  # 十六进制表示，前缀0x

print("二进制表示:", binary)
print("八进制表示:", octal)
print("十进制表示:", decimal)
print("十六进制表示:", hexadecimal)